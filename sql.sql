 GROUP BY MODEL_ID, STRATEGY_ID, BATNO, FIELD_ID, VAL;
    
 SELECT 
	@row_number:=CASE
	WHEN @customer_no = s.age
   FROM 
	TB_STAT_FIELD_RECORD s, (SELECT @row_number:=0, @customer_no:=0) AS t
ORDER BY S.BATNO, S.FIELD_ID, S.RULE_ID;